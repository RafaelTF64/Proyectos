public class Proyecto1 {

		public static void main(String[] args) {
			
			int num = 5;
			System.out.println(num);
			
			float temp = 5.7f;
			System.out.println(temp);
			
			char semana = 'x';
			System.out.println(semana);
			
			//más preciso que Float, permite más decimales
			double otrodecimal = 7.5d;
			System.out.println(otrodecimal);
			
			
			//más raro
			//guarda verdadero o falso
			boolean hacefrio;
			
			hacefrio = true;
			
			System.out.println(hacefrio);
			
			
		}
}
